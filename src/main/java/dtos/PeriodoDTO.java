
package dtos;

public class PeriodoDTO {
    
    private String tipo_periodo;
    private int anio_lectivo;

    public String getTipo_periodo() {
        return tipo_periodo;
    }

    public void setTipo_periodo(String tipo_periodo) {
        this.tipo_periodo = tipo_periodo;
    }

    public int getAnio_lectivo() {
        return anio_lectivo;
    }

    public void setAnio_lectivo(int anio_lectivo) {
        this.anio_lectivo = anio_lectivo;
    }
    
}
